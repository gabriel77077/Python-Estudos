#Para ler dois valores reais do teclado, calcular e imprimir na tela:
#a) A soma destes valores
#b) O produto deles
#c) O quociente entre eles

valor01 = input("Digite um valor real: ")
valor02 = input("Digite outro valor real: ")

valor01 = float(valor01)
valor02 = float(valor02)

print("Soma: ",valor01 + valor02)
print("Produto: ",valor01 * valor02)
print("Quociente: ",valor01 / valor02)
